<template>
  <section class="container">
      <div v-if="post">
          <h1>{{ post.title }}</h1>
          <p>{{post.content}}</p>
      </div>
      <div v-else>
          Loading..
      </div>
  </section>
</template>

<script>
import axios from 'axios'

export default {
    name: 'PostDetail',
    data(){
        return{
            post: null,
        }
    },
    created(){
       this.getPostDetail();
    },
    methods:{
        getPostDetail(){
            axios.get(`http://127.0.0.1:8000/api/posts/${this.$route.params.slug}`)
                .then( res => {
                    console.log(res.data);
                    this.post = res.data;
                })
                .catch(err => log.error(err));
        }
    }
}
</script>

